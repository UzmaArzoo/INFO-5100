    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        
        Type type = (Type) organizationJComboBox.getSelectedItem();
        for(Organization  org : this.ep.getOrganizationDirectory().getOrganizationList())
        {
            if(org.getName().equals(type.getValue()))
            {
                JOptionPane.showMessageDialog(null,"This organsization already exist"); return;
            }
        }
        directory.createOrganization(type);
        JOptionPane.showMessageDialog(null,"Organsization Added");
        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed

