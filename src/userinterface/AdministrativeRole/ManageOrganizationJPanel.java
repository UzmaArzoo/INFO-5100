    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        if(et.getValue().equals(EnterpriseType.Embassy.getValue()))
        {
            for (Organization.Type type : Organization.Type.values()){
            if (type.getValue().equals(Organization.Type.Approval.getValue()))
                organizationJComboBox.addItem(type);
            
            if (type.getValue().equals(Organization.Type.Checking.getValue()))
                organizationJComboBox.addItem(type);
            }
            
        }
       
        if(et.getValue().equals(EnterpriseType.Ngo.getValue()))
        {
           for (Organization.Type type : Organization.Type.values()){
            if (type.getValue().equals(Organization.Type.Food.getValue()))
                organizationJComboBox.addItem(type);
            
            if (type.getValue().equals(Organization.Type.Health.getValue()))
                organizationJComboBox.addItem(type);
            }
        }
        
        if(et.getValue().equals(EnterpriseType.Uno.getValue()))
        {
           for (Organization.Type type : Organization.Type.values()){
            if (type.getValue().equals(Organization.Type.Registration.getValue()))
                organizationJComboBox.addItem(type);
            
            if (type.getValue().equals(Organization.Type.Manager.getValue()))
                organizationJComboBox.addItem(type);
            }
        }
        if(et.getValue().equals(EnterpriseType.ImmigrationOffice.getValue()))
        {
           for (Organization.Type type : Organization.Type.values()){
            if (type.getValue().equals(Organization.Type.Officer.getValue()))
                organizationJComboBox.addItem(type);
            
            }
        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        System.out.println(directory.getOrganizationList().size());
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
